"""RSP-aligned Risk Report generator.

Generates safety risk reports aligned with Anthropic's Responsible
Scaling Policy (RSP) v3.0 framework. Produces structured assessments
covering threat models, safety profiles, and risk mitigations.

Usage:
    from sentinel.rsp_report import RiskReportGenerator

    generator = RiskReportGenerator()
    report = generator.generate(texts=["user input 1", "user input 2"])
    print(report.to_markdown())
"""

from __future__ import annotations

from dataclasses import dataclass, field
from datetime import datetime, timezone
from typing import Sequence

from sentinel.core import SentinelGuard, ScanResult, RiskLevel


@dataclass
class ThreatAssessment:
    domain: str
    description: str
    risk_level: RiskLevel
    findings_count: int
    mitigations: list[str]


@dataclass
class RiskReport:
    """Structured risk report aligned with Anthropic RSP v3.0."""

    generated_at: str
    total_scans: int
    overall_risk: RiskLevel
    blocked_count: int
    threat_assessments: list[ThreatAssessment]
    risk_distribution: dict[str, int]
    category_breakdown: dict[str, int]
    scan_latency_avg_ms: float
    recommendations: list[str]

    def to_markdown(self) -> str:
        lines = [
            "# Safety Risk Report",
            f"*Generated: {self.generated_at}*",
            "",
            "## Executive Summary",
            "",
            f"- **Overall Risk Level**: {self.overall_risk.value.upper()}",
            f"- **Total Scans**: {self.total_scans}",
            f"- **Content Blocked**: {self.blocked_count}",
            f"- **Avg Scan Latency**: {self.scan_latency_avg_ms:.2f}ms",
            "",
            "## Risk Distribution",
            "",
            "| Risk Level | Count |",
            "|------------|-------|",
        ]
        for level in ["critical", "high", "medium", "low", "none"]:
            count = self.risk_distribution.get(level, 0)
            lines.append(f"| {level.upper()} | {count} |")

        lines.extend([
            "",
            "## Threat Domain Assessments",
            "",
        ])

        for assessment in self.threat_assessments:
            lines.extend([
                f"### {assessment.domain}",
                "",
                f"**Risk Level**: {assessment.risk_level.value.upper()}  ",
                f"**Findings**: {assessment.findings_count}",
                "",
                assessment.description,
                "",
            ])
            if assessment.mitigations:
                lines.append("**Active Mitigations:**")
                for m in assessment.mitigations:
                    lines.append(f"- {m}")
                lines.append("")

        if self.category_breakdown:
            lines.extend([
                "## Finding Categories",
                "",
                "| Category | Count |",
                "|----------|-------|",
            ])
            for cat, count in sorted(
                self.category_breakdown.items(),
                key=lambda x: x[1],
                reverse=True,
            ):
                lines.append(f"| {cat} | {count} |")
            lines.append("")

        if self.recommendations:
            lines.extend([
                "## Recommendations",
                "",
            ])
            for i, rec in enumerate(self.recommendations, 1):
                lines.append(f"{i}. {rec}")
            lines.append("")

        lines.extend([
            "---",
            "*Report generated by Sentinel AI — aligned with "
            "Anthropic Responsible Scaling Policy (RSP) v3.0 framework.*",
        ])

        return "\n".join(lines)

    def to_dict(self) -> dict:
        return {
            "generated_at": self.generated_at,
            "total_scans": self.total_scans,
            "overall_risk": self.overall_risk.value,
            "blocked_count": self.blocked_count,
            "threat_assessments": [
                {
                    "domain": a.domain,
                    "description": a.description,
                    "risk_level": a.risk_level.value,
                    "findings_count": a.findings_count,
                    "mitigations": a.mitigations,
                }
                for a in self.threat_assessments
            ],
            "risk_distribution": self.risk_distribution,
            "category_breakdown": self.category_breakdown,
            "scan_latency_avg_ms": self.scan_latency_avg_ms,
            "recommendations": self.recommendations,
        }


