class RiskReportGenerator:
    """Generate RSP-aligned risk reports from scan results."""

    def __init__(self, guard: SentinelGuard | None = None):
        self._guard = guard or SentinelGuard.default()

    def generate(
        self,
        texts: Sequence[str] | None = None,
        scan_results: Sequence[ScanResult] | None = None,
    ) -> RiskReport:
        """Generate a risk report from texts or pre-computed scan results."""
        if scan_results is None:
            if texts is None:
                raise ValueError("Provide either texts or scan_results")
            scan_results = [self._guard.scan(t) for t in texts]

        results = list(scan_results)

        # Aggregate metrics
        total = len(results)
        blocked = sum(1 for r in results if r.blocked)
        latency_avg = (
            sum(r.latency_ms for r in results) / total if total > 0 else 0.0
        )

        # Risk distribution
        risk_dist: dict[str, int] = {}
        for r in results:
            level = r.risk.value
            risk_dist[level] = risk_dist.get(level, 0) + 1

        # Category breakdown
        category_counts: dict[str, int] = {}
        for r in results:
            for f in r.findings:
                category_counts[f.category] = (
                    category_counts.get(f.category, 0) + 1
                )

        # Overall risk
        overall = RiskLevel.NONE
        for r in results:
            if r.risk > overall:
                overall = r.risk

        # Threat domain assessments
        domain_findings: dict[str, int] = {}
        domain_max_risk: dict[str, RiskLevel] = {}
        for r in results:
            for f in r.findings:
                domain = _CATEGORY_TO_DOMAIN.get(f.category, "Other")
                domain_findings[domain] = domain_findings.get(domain, 0) + 1
                current = domain_max_risk.get(domain, RiskLevel.NONE)
                if f.risk > current:
                    domain_max_risk[domain] = f.risk

        # Build assessments for all domains
        assessments = []
        for domain in _DOMAIN_DESCRIPTIONS:
            count = domain_findings.get(domain, 0)
            risk = domain_max_risk.get(domain, RiskLevel.NONE)
            assessments.append(
                ThreatAssessment(
                    domain=domain,
                    description=_DOMAIN_DESCRIPTIONS[domain],
                    risk_level=risk,
                    findings_count=count,
                    mitigations=_DOMAIN_MITIGATIONS.get(domain, []),
                )
            )

        # Sort by risk level (highest first)
        risk_order = list(RiskLevel)
        assessments.sort(
            key=lambda a: risk_order.index(a.risk_level), reverse=True
        )

        # Generate recommendations
        recommendations = self._generate_recommendations(
            overall, category_counts, blocked, total
        )

        return RiskReport(
            generated_at=datetime.now(timezone.utc).strftime(
                "%Y-%m-%d %H:%M:%S UTC"
            ),
            total_scans=total,
            overall_risk=overall,
            blocked_count=blocked,
            threat_assessments=assessments,
            risk_distribution=risk_dist,
            category_breakdown=category_counts,
            scan_latency_avg_ms=round(latency_avg, 2),
            recommendations=recommendations,
        )

    @staticmethod
    def _generate_recommendations(
        overall: RiskLevel,
        categories: dict[str, int],
        blocked: int,
        total: int,
    ) -> list[str]:
        recs = []

        if categories.get("prompt_injection", 0) > 0:
            recs.append(
                "Implement input validation and prompt hardening to reduce "
                "injection attack surface"
            )
        if categories.get("pii", 0) > 0:
            recs.append(
                "Enable automatic PII redaction in production pipelines "
                "to prevent data leakage"
            )
        if categories.get("harmful_content", 0) > 0:
            recs.append(
                "Review content policy configuration and ensure harmful "
                "content blocking is enabled at appropriate thresholds"
            )
        if categories.get("tool_use", 0) > 0:
            recs.append(
                "Audit agentic tool-use permissions and restrict shell "
                "execution to allowlisted commands"
            )
        if categories.get("hallucination", 0) > 0:
            recs.append(
                "Consider adding retrieval-augmented generation (RAG) or "
                "citation verification for factual accuracy"
            )
        if categories.get("toxicity", 0) > 0:
            recs.append(
                "Review output filtering thresholds for toxicity and "
                "adjust profanity risk levels for your use case"
            )
        if blocked > 0 and total > 0:
            block_rate = blocked / total * 100
            recs.append(
                f"Block rate is {block_rate:.1f}% — review blocked content "
                f"to calibrate sensitivity thresholds"
            )
        if overall >= RiskLevel.HIGH:
            recs.append(
                "Overall risk is HIGH or above — consider escalation "
                "procedures and incident response planning"
            )
        if not recs:
            recs.append("No significant risks detected in this assessment")

        return recs
